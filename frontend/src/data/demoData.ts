// Helper functions to get specific data by ID
export const getUserById = (id: string) => users.find(u => u.id === id);
export const getPostById = (id: string) => posts.find(p => p.id === id);
export const getNeighborhoodById = (id: string) => neighborhoods.find(n => n.id === id);
export const getMessageThreadById = (id: string) => messageThreads.find(t => t.id === id);
export const getCommentsByPostId = (postId: string) => comments.filter(c => c.postId === postId);
export const getMessagesByThreadId = (threadId: string) => messages.filter(m => m.threadId === threadId);

// Helper function to get posts by user
export const getPostsByUserId = (userId: string) => posts.filter(p => p.userId === userId);

// Helper function to get active posts
export const getActivePosts = () => posts.filter(p => p.status === 'active');

// Helper function to get posts by neighborhood
export const getPostsByNeighborhood = (neighborhoodId: string) => {
  return posts.filter(post => {
    const user = getUserById(post.userId);
    return user?.location.type === 'neighborhood' && user.location.neighborhoodId === neighborhoodId;
  });
};

// Helper function to get users by neighborhood
export const getUsersByNeighborhood = (neighborhoodId: string) => {
  return users.filter(u => u.location.type === 'neighborhood' && u.location.neighborhoodId === neighborhoodId);
};

