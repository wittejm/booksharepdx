// Helper function to generate IDs
let idCounter = 0;
const generateId = (prefix: string) => `${prefix}_${++idCounter}_${Date.now()}`;

// Helper function to get random date in the past
const getRandomPastDate = (daysAgo: number) => {
  const now = Date.now();
  return now - Math.floor(Math.random() * daysAgo * 24 * 60 * 60 * 1000);
};

// Helper function to get random item from array
const randomItem = <T>(arr: T[]): T => arr[Math.floor(Math.random() * arr.length)];

// Helper function to get random items from array
const randomItems = <T>(arr: T[], count: number): T[] => {
  const shuffled = [...arr].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

